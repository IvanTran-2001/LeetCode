class Solution(object):
    def replaceWords(self, dictionary, sentence):
        """
        :type dictionary: List[str]
        :type sentence: str
        :rtype: str
        """
        my_dict = {}
        for word in dictionary:
            temp = my_dict
            if word == "i":
                ''  
            for i in range(len(word)):
                letter = word[i]
                
                if not(letter in temp):
                    temp[word[i]] = dict()
                    
                elif 'Smallest' in temp[word[i]]:
                    break
                
                if len(word) - 1 == i:
                    temp[word[i]] = dict()
                    temp[word[i]]["Smallest"] = None
                    
                temp = temp[word[i]]
            
        new_array = sentence.split(' ')
        
        for i in range(len(new_array)):
            temp = my_dict
            count = 1
            for j in range(len(new_array[i])):
                if new_array[i][j] in temp:
                    temp = temp[new_array[i][j]]
                else:
                    count -= 1
                    break
            
            
            if "Smallest" in temp:
                new_array[i] = new_array[i][:j + count]
                    
        return " ".join(new_array)
                

